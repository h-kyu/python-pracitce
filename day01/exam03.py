print(100, 10, 'hello')
print("hello" + " python")
# print(2 + "python") 숫자랑 문자랑은 더하기가 되지 않는다
print(1,10,100, sep='!!!', end='')
# sep: 콤마로 생겨 출력되는 공백에 sep에 입력된 값들이 포함
# end: 맨 뒤에 붙여지는 값, 뒤에 print와 이어쓰기
print("\"my name is hong\"") 
# "를 사용하기 위해선 \ 붙이기
print('"my name is hong"')
# "를 사용하기 위해선 시작 부호를 '로 다르게 붙이기
print('I\'m hong')
print("I'm hong")
